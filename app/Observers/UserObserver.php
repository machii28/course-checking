namespace App\Observers;

use App\Models\Professor;
use App\Models\Student;
use App\Models\User;

class UserObserver
{
    /**
     * Handle the User "created" event.
     */
    public function created(User $user): void
    {
        if ($user->role === 'professor') {
            Professor::create([
                'user_id' => $user->id
            ]);
        }

        if ($user->role === 'student') {
            Student::create([
                'user_id' => $user->id
            ]);
        }
    }

